<?php

namespace App\Mail;

use Illuminate\Bus\Queueable;
use Illuminate\Contracts\Queue\ShouldQueue;
use Illuminate\Mail\Mailable;
use Illuminate\Queue\SerializesModels;

class UserCancelOrderToRestaurantMail extends Mailable
{
    use Queueable, SerializesModels;

    public $data;

    /**
     * Create a new message instance.
     *
     * @return void
     */
    public function __construct($data)
    {
        $this->data = $data;
    }

    /**
     * Build the message.
     *
     * @return $this
     */
    public function build()
    {
        $subject = 'El usuario canceló el pedido';
        return $this->markdown('emails.orders.userCancelOrderToRestaurant')->subject($subject)->from(env('MAIL_FROM_ADDRESS'), 'Pedí Simple');
    }
}
